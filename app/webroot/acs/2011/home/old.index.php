<h1>Overview</h1>

<p>
  We invite submissions to the First International Conference on
  Cognitive Systems, which will take place in Arlington, Virginia on
  November 4, 5, and 6, 2011. The new meeting will bring together
  researchers with interests in human-level intelligence, complex
  cognition, integrated intelligent systems, cognitive architectures,
  and related topics. This event harks a return to the initial goals of
  artificial intelligence and cognitive science, which aimed to explain
  intelligence in computational terms and reproduce the entire range of
  human cognitive abilities in computational artifacts.
</p>

<p>
  Because many researchers remain committed to this original vision,
  there is need for a conference that provides a place to present
  recent results and pose new challenges for the field. However,
  because this community retains many ties to mainstream artificial
  intelligence and cognitive science, annual meetings will be
  colocated with existing conferences associated with the AAAI and
  Cognitive Science communities.  The initial meeting will double as
  one of the 2011 AAAI Fall Symposia.
</p>

<p>
  The conference will offer a venue to report work on any topic
  related to the representation or organization of complex mental
  structures, their use in multi-step cognition, or their acquisition
  from experience or instruction. Some functional capabilities that
  arise in this context include:
<p>

